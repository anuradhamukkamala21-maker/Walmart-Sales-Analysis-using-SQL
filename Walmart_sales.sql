# 📊 Walmart Sales Analysis – SQL Queries with Outputs

------------------------------------------------------------
1. TOTAL SALES
------------------------------------------------------------

Syntax:
SELECT SUM(Weekly_Sales) AS Total_Sales
FROM walmart;

Expected Output:

+----------------+
| Total_Sales    |
+----------------+
| 6737218987.11  |
+----------------+

Explanation:
Calculates the total revenue generated across all Walmart stores.

------------------------------------------------------------
2. SALES BY STORE
------------------------------------------------------------

Syntax:
SELECT Store,
       SUM(Weekly_Sales) AS Sales
FROM walmart
GROUP BY Store
ORDER BY Sales DESC;

Expected Output:

+-------+---------------+
| Store | Sales         |
+-------+---------------+
| 20    | 301397792.46  |
| 4     | 299543953.38  |
| 14    | 288999911.34  |
+-------+---------------+

Explanation:
Displays total sales generated by each store.

