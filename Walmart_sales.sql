9. STORE RANKING USING WINDOW FUNCTION
------------------------------------------------------------

Syntax:
SELECT Store,
       SUM(Weekly_Sales) AS Total_Sales,
       RANK() OVER (ORDER BY SUM(Weekly_Sales) DESC) AS Store_Rank
FROM walmart
GROUP BY Store;

Expected Output:

+-------+---------------+-------------+
| Store | Total_Sales   | Store_Rank |
+-------+---------------+-------------+
| 20    | 301397792.46  | 1          |
| 4     | 299543953.38  | 2          |
| 14    | 288999911.34  | 3          |
+-------+---------------+-------------+

Explanation:
Ranks stores according to total sales.

------------------------------------------------------------
10. MOVING AVERAGE ANALYSIS
------------------------------------------------------------

Syntax:
SELECT Date,
       Weekly_Sales,
       AVG(Weekly_Sales) OVER (
           ORDER BY Date
           ROWS BETWEEN 4 PRECEDING AND CURRENT ROW
       ) AS Moving_Avg
FROM walmart;

Expected Output:

+------------+---------------+--------------+
| Date       | Weekly_Sales  | Moving_Avg   |
+------------+---------------+--------------+
| 2010-02-05 | 1643690.90    | 1643690.90  |
| 2010-02-12 | 1641957.44    | 1642824.17  |
| 2010-02-19 | 1611968.17    | 1632538.84  |
+------------+---------------+--------------+

Explanation:
Calculates moving averages to identify smoother sales trends.

